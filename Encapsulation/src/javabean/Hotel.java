public class Hotel {
	
	private String name;
	private int rating;
	
	public void setName(String name) {
		
		this.name=name;
	}
	
	public String getName(){
		return name;		
	}
	
	public void setRating(int rating) {
		this.rating=rating;
	}
	public int getRating() {
		return rating;
	}

}
